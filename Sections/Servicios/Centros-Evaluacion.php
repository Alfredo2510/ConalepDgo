<h2 class="general">Centros de Evaluación</h2>

<p class="general">Considerando la necesidad de que las empresas adopten las nuevas formas de organización del trabajo y de gestión de sus Recursos Humanos, el Sistema CONALEP, a través de los 2 Centros de Evaluación con que cuenta en nuestro estado, pone a disposición del sector productivo servicios de evaluación y certificación  de competencia laboral que permiten evaluar y certificar el desempeño laboral de los trabajadores. <br><br>  
El CONALEP como una de las instituciones pioneras y más importante en la prestación de Servicios de Evaluación con fines de Certificación de Competencias Laborales, se acredita ante el CONOCER como Entidad de Certificación y Evaluación de Competencias (ECE) el 26 de agosto de 2010 con número de cédula ECE013-10, con el fin de ser parte activa del Sistema Nacional de Competencias. <br><br>  
Los servicios de evaluación y certificación que ofrece el Sistema  CONALEP se apegan a los documentos emitidos por el Consejo Nacional de Normalización y Certificación de Competencia  Laboral   (CONOCER).</p>

