<p class="general">
    La evaluación de competencia laboral permite identificar las habilidades, destrezas y conocimientos de una persona durante el desarrollo de una actividad laboral específica; tomando como referente los Estándares de Competencia. <br> <br>
    En Durango el Sistema CONALEP tiene acreditaciones en los siguientes Estándares:
</p>
<ol class="general" id="CE-List">
	<li>EC0011    Elaboración de documentos mediante un procesador de textos.</li>
	<li>EC0012    Elaboración de presentaciones gráficas mediante herramientas de cómputo.</li>
	<li>EC0013    Elaboración de libros mediante el uso de procesadores de hojas de cálculo.</li>
    <li>EC0217    Impartición de cursos de formación del capital humano de manera presencial grupal.</li>
</ol>
