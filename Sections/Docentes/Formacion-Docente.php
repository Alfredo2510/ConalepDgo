<h2 class="general">Formación Docente</h2>

<p class="general">En el Modelo Académico del CONALEP  incorpora la formación en competencias genéricas, disciplinares y profesionales, por ello es indispensable el conocimiento y la experiencia práctica de los docentes, es por ello que su formación docente está orientada al desarrollo de éstas competencias para lograr establecer un puente entre el ámbito educativo y la vida cotidiana.
Es importante mencionar además que el reto de los docentes en el Sistema Conalep incluye atender población de alto riesgo, diversificación cultural de los alumnos, grupos heterogéneos, inequidad en el acceso al conocimiento y saberes en permanente evolución disciplinar, cultural, laboral y social, por lo que debe estar preparado y mantenerse en constante actualización.
</p>

