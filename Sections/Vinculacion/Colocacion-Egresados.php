<h2 class="general">Colocación de Egresados</h2>

<span class="general-MV" >Colocación de Egresados</span>

<p class="general">CONALEP tiene el compromiso con sus egresados en proporcionarles los elementos necesarios para lograr su colocación en el mercado laboral, por ello, te invitamos a que acudas a la Jefatura de Proyecto de Promoción y Vinculación, y se proporcionara la información necesaria de las vacantes en las diversas Instituciones y/o empresas. <br> <br>

Propiamente, la Bolsa de Trabajo se encamina a ofrecer alternativas de empleo a los egresados de CONALEP, incorporándolos al mercado laboral en la zona de influencia de cada uno de los planteles. El servicio se ofrece de forma gratuita a los egresados. <br><br>

<span class="general-MV" >Seguimiento a Egresados</span>
<p class="general">Ante la necesidad de evaluar la calidad y pertinencia de la educación, así como la inserción laboral y la situación socioeconómica de los egresados del CONALEP, en función a una realidad que cambia rápidamente, se realiza cada año el Estudio de Seguimiento de Egresados del CONALEP que consiste en tres etapas, para poder conocer:</p>
</p>



<ol class="general" id="AC-general">
<li>1.	Realización de un censo al momento de su egreso.</li>
	<li>2.	Identificar el proceso de inserción laboral de los egresados en un periodo de 12 meses posterior a su egreso, a través de la aplicación de una encuesta persona a persona, y.</li>
    <li>3.	Conocer su trayectoria laboral y educativa de los egresados en un periodo de cinco años posterior a su egreso, a través de la aplicación de una encuesta persona a persona.</li>

</ol>

