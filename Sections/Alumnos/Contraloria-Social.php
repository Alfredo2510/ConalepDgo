<h2 class="general">Contraloria Social</h2>

<span class="general-MV">OBJETIVO GENERAL</span>
<p class="general">Favorecer la participación activa de la comunidad estudiantil beneficiada para supervisar y vigilar que las actividades y procesos llevados a cabo por los Comités de Becas se proporcionen con transparencia, oportunidad y calidad en apego a la normatividad vigente y bajo las responsabilidades establecidas en sus ámbitos de competencia. </p>

<span class="general-MV">OBJETIVOS ESPECÍFICOS</span>
<ol class="general">
	<li>Promover la participación de la comunidad estudiantil beneficiaria del Programa de Becas del CONALEP en el establecimiento de la Contraloría Social y Vigilancia.</li>
	<li>Informar a los beneficiarios acerca de las becas otorgadas y el proceso para su solicitud, validación, asignación, pago y comprobación del recurso.</li>
	<li>Impulsar la transparencia en el correcto uso del presupuesto asignado al Programa de Becas del CONALEP.</li>
    <li>Instrumentar mecanismos para la captación y atención de consultas, quejas, denuncias y sugerencias al proceso de entrega del Programa de Becas del CONALEP.</li>
    <li>Captar la opinión de los becarios respecto de la operación del Programa de Becas del CONALEP para su actualización y mejora.</li>
</ol>

