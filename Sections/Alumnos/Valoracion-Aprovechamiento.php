<h2 class="general">Valoración del Aprovechamiento Académico </h2>
<p class="general">Como una acción preventiva para disminuir la reprobación y abandono escolar, en CONALEP se realiza el seguimiento al aprovechamiento académico, mediante el Sistema de Administración Escolar (SAE), mediante cortes semestrales de información de los alumnos respecto de sus evaluaciones y asistencias a la 6ª,11ª y 16ª semanas de haber iniciado el ciclo semestral, con ello se puede identificar a los alumnos que están en riesgo de abandono escolar, así como que los docentes del Sistema registren las evaluaciones y asistencias de sus alumnos en tiempo, lo que permite dar un seguimiento personalizado por alumno y por ende establecer estrategias de seguimiento y retención de los mismos.</p>

