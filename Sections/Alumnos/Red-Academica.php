<h2 class="general">Red Académica</h2>
<p class="general">
La Red Académica es un espacio virtual en el que encontrarás información relevante y actualizada sobre recursos didácticos, ambientes y comunidades educativas y que facilita el proceso de comunicación e interacción entre la comunidad del Sistema CONALEP, en congruencia con la tendencia de incorporar las Tecnologías de la Información y la Comunicación en los procesos de enseñanza aprendizaje.
En este espacio podrás consultar libros, manuales, objetos de aprendizaje, videoprogramas, software educativo, páginas web y tutoriales interactivos elaborados especialmente para apoyar el proceso de enseñanza aprendizaje; asimismo, encontrarás información sobre el uso y aprovechamiento de los laboratorios multipropósito, simuladores humanos y bibliotecas.
Mediante la participación activa y creativa de toda la comunidad, la Red Académica se convertirá en un espacio para gestionar los recursos y ambientes de aprendizaje, promover el intercambio de experiencias y propuestas educativas y una mayor integración del Sistema Conalep. Por ello, te invitamos a formar parte de la Red Académica del CONALEP.
</p>
