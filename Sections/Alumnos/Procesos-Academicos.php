<h2 class="general">Procesos Académicos</h2>

<p class="general">El Colegio Nacional de Educación Profesional Técnica tiene por objeto la impartición de educación profesional técnica con la finalidad de satisfacer la demanda de personal técnico calificado para el sistema productivo del país, así como educación de bachillerato dentro del tipo medio superior a fin de que los estudiantes puedan continuar con otro tipo de estudios.
Por tal motivo es necesario contar con un instrumento normativo que establezca las reglas que regirán las obligaciones, así como garantizar sus derechos desde el ingreso hasta el egreso de los alumnos.
</p>

